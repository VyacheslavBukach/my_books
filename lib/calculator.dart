class Calculator {
  double sum({required double number1, required double number2}) {
    return number1 + number2;
  }

  double difference({required double number1, required double number2}) {
    return number1 - number2;
  }

  double multiply({required double number1, required double number2}) {
    return number1 * number2;
  }

  double divide({required double number1, required double number2}) {
    return number1 / number2;
  }
}
